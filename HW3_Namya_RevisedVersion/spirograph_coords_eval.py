import math

R=36
r=9
a=30

# Tommy Trojan's Coordinates
# 34° 1'14.00"N   34.020556
# 118°17'7.57"W   -118.285436

nRev = 8
t = 0.00
X_tt = -118.285436
Y_tt = 34.020556

def remove(string):
    return "".join(string.split())

while(t<(math.pi*nRev)):
        x = X_tt + 0.0001*(R+r)*math.cos((r/R)*t) - 0.0001*a*math.cos((1+r/R)*t)
        y = Y_tt + 0.0001*(R+r)*math.sin((r/R)*t) - 0.0001*a*math.sin((1+r/R)*t)

        t+=0.01
        s=str('%0.6f'%x)+","+str('%0.6f'%y)+","+str(100)
        print(remove(s))